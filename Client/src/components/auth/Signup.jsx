import React, { useEffect, useState } from 'react'
import Navbar from '../shared/Navbar'
import { Label } from '../ui/label'
import { Input } from '../ui/input'
import { RadioGroup } from '../ui/radio-group'
import { Button } from '../ui/button'
import { Link, useNavigate } from 'react-router-dom'
import axios from 'axios'
import { USER_API_END_POINT } from '@/utils/constant'
import { toast } from 'sonner'
import { useDispatch, useSelector } from 'react-redux'
import { setLoading } from '@/redux/authSlice'
import { Loader2 } from 'lucide-react'

const Signup = () => {
  const navigate = useNavigate()
  const dispatch = useDispatch()
  const { loading, user } = useSelector(store => store.auth)

  const [input, setInput] = useState({
    fullname: "",
    email: "",
    password: "",
    phoneNumber: "",
    role: "",
    file: "",
  })

  const changeEventHandler = (e) => setInput({ ...input, [e.target.name]: e.target.value })
  const changeFileHandler = (e) => setInput({ ...input, file: e.target.files?.[0] })

  const submitHandler = async (e) => {
    e.preventDefault()
    if (!input.fullname || !input.email || !input.password || !input.phoneNumber || !input.role) {
      return toast.error("Please fill all fields")
    }

    const formData = new FormData()
    formData.append("fullname", input.fullname)
    formData.append("email", input.email)
    formData.append("password", input.password)
    formData.append("phoneNumber", input.phoneNumber)
    formData.append("role", input.role)
    if (input.file) formData.append("file", input.file)

    try {
      dispatch(setLoading(true))
      const response = await axios.post(`${USER_API_END_POINT}/register`, formData, {
        headers: { "Content-Type": "multipart/form-data" },
        withCredentials: true, // âœ… important
      })
      if (response.data.success) {
        toast.success(response.data.message)
        navigate("/login")
      }
    } catch (error) {
      console.log(error)
      toast.error(error?.response?.data?.message || "Something went wrong")
    } finally {
      dispatch(setLoading(false))
    }
  }

  useEffect(() => {
    if (user) navigate("/")
  }, [user, navigate])

  return (
    <div>
      <Navbar />
      <div className="flex items-center justify-center max-sm:mx-5 mx-auto">
        <form onSubmit={submitHandler} className="w-full sm:w-1/2 border border-gray-200 rounded-md p-4 my-10">
          <h1 className="font-bold text-xl mb-5">Sign Up</h1>

          <div className="my-2">
            <Label>Full Name</Label>
            <Input type="text" name="fullname" value={input.fullname} onChange={changeEventHandler} placeholder="John Doe" />
          </div>

          <div className="my-2">
            <Label>Email</Label>
            <Input type="email" name="email" value={input.email} onChange={changeEventHandler} placeholder="john.doe@gmail.com" />
          </div>

          <div className="my-2">
            <Label>Phone Number</Label>
            <Input type="text" name="phoneNumber" value={input.phoneNumber} onChange={changeEventHandler} placeholder="0000000000" />
          </div>

          <div className="my-2">
            <Label>Password</Label>
            <Input type="password" name="password" value={input.password} onChange={changeEventHandler} placeholder="12345678" />
          </div>

          <div className="flex items-center justify-between">
            <RadioGroup className="flex items-center gap-4 my-5">
              <div className="flex items-center space-x-2">
                <Input type="radio" name="role" value="student" checked={input.role === "student"} onChange={changeEventHandler} />
                <Label>Job Seeker</Label>
              </div>
              <div className="flex items-center space-x-2">
                <Input type="radio" name="role" value="recruiter" checked={input.role === "recruiter"} onChange={changeEventHandler} />
                <Label>Employer</Label>
              </div>
            </RadioGroup>

            <div className="flex items-center gap-2">
              <Label>Profile</Label>
              <Input accept="image/*" type="file" onChange={changeFileHandler} className="cursor-pointer" />
            </div>
          </div>

          {loading ? (
            <Button className="my-4 w-full">
              <Loader2 className="mr-2 h-4 w-4 animate-spin" /> Please wait
            </Button>
          ) : (
            <Button type="submit" className="w-full my-4">Signup</Button>
          )}

          <span className="text-sm">
            Already have an account? <Link to="/login" className="text-blue-600">Login</Link>
          </span>
        </form>
      </div>
    </div>
  )
}

export default Signup







